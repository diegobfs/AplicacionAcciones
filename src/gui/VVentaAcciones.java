    private void actualizarTabla(String empresa) {
        DefaultTableModel modelo = new DefaultTableModel();
        Iterator<Empresa> empresasIterator = fa.consultarOfertas(empresa,usuario.getId_usuario()).iterator();
        Empresa auxiliar;
        modelo.addColumn("IdEmpresa");
        modelo.addColumn("CIF Empresa");
        modelo.addColumn("Cantidad Acciones");
        
        while (empresasIterator.hasNext()) {
            auxiliar = empresasIterator.next();
        
            modelo.addRow(new Object[]{auxiliar.getUsuario().getId_usuario(),
                auxiliar.getCIF(), auxiliar.getTelefono()}); 
        }
        
        jTable1.setModel((TableModel) modelo);
        if (modelo.getRowCount() > 0) {
            jTable1.setRowSelectionInterval(0, 0);
        }
    }
   
    


