    private void botonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonComprarActionPerformed
        // TODO add your handling code here:
        
        try{
            int filaSeleccionada = jTable1.getSelectedRow();
            
            int numeroAcciones = Integer.parseInt(numeroCompra.getText());
            Usuario u=fa.consultarUsuarios(usuario.getId_usuario()).get(0);
            int accionesTabla = Integer.parseInt(jTable1.getValueAt(filaSeleccionada, 2).toString());
            
            float precioM = Float.parseFloat(precio.getText());

            if(numeroAcciones <= accionesTabla && numeroAcciones > 0){
                fa.Comprar(usuario, numeroAcciones, jTable1.getValueAt(filaSeleccionada, 0).toString(), precioM);
                iventana.onSuccess();
                actualizarTabla("");
            }else{
                fa.muestraExcepcion("No hay suficientes acciones a la venta o el número insertado no es correcto.");

            }
        }catch(NumberFormatException e) {
            fa.muestraExcepcion("Inserte un número");
        }
    }//GEN-LAST:event_botonComprarActionPerformed
     
