    //Vaciar tabla
    private void vaciarTabla() {
        int rowCount = TablaOfertaVenta.getModel().getRowCount();
        DefaultTableModel modelo = (DefaultTableModel) TablaOfertaVenta.getModel();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
        TablaOfertaVenta.setModel(modelo);
    }

    //Actualiza el contenido de la tabla
    private void BuscarTabla(String empresa) {
        DefaultTableModel modelo = new DefaultTableModel();
        Iterator<HacerOfertaVenta> ofertasAccionesIterator = fa.filtrarOfertasUsuario(usuario, empresa).iterator();
        HacerOfertaVenta auxiliar;

        modelo.addColumn("Precio");
        modelo.addColumn("FechaOferta");
        modelo.addColumn("Numero de participaciones");
        modelo.addColumn("Disponibilidad");
        modelo.addColumn("Nombre Comercial");
        modelo.addColumn("CIF");
        modelo.addColumn("IDEmpresa");

        while (ofertasAccionesIterator.hasNext()) {
            auxiliar = ofertasAccionesIterator.next();

            modelo.addRow(new Object[]{
                auxiliar.getPrecio(),
                auxiliar.getFechaOferta(),
                auxiliar.getNumeroParticipaciones(),
                auxiliar.isDisponibilidad(),
                auxiliar.getEmpresa().getNombreComercial(),
                auxiliar.getEmpresa().getCIF(),
                auxiliar.getEmpresa().getUsuario().getId_usuario()});
        }

        TablaOfertaVenta.setModel((TableModel) modelo);
        if (modelo.getRowCount() > 0) {
            TablaOfertaVenta.setRowSelectionInterval(0, 0);
        }
    }

