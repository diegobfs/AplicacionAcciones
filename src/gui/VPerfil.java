    public void guardarDatosUsuario() {
        if (!TextoNombre.getText().isEmpty() && !TextoAp1.getText().isEmpty() && !TextoAp2.getText().isEmpty() && !TextoID.getText().isEmpty()
                && !TextoCalle.getText().isEmpty() && !TextoPortal.getText().isEmpty() && !TextoPiso.getText().isEmpty() && !TextoPuerta.getText().isEmpty() && !TextoTelefono.getText().isEmpty()) {
            try {
                //Comprobación errores a int
                int Telefono;
                Telefono = Integer.parseInt(this.TextoTelefono.getText());
                int portal = Integer.parseInt(TextoPortal.getText());
                int piso = Integer.parseInt(TextoPiso.getText());
                
                if (piso>0 && portal > 0 && Telefono > 0){
                    Inversor newInversor = new Inversor(fa.consultarUnUsuario(idAntiguo), TextoDNI.getText(), TextoNombre.getText(), TextoAp1.getText(),
                            TextoAp2.getText(), TextoCalle.getText(), Integer.parseInt(TextoPortal.getText()), Integer.parseInt(TextoPiso.getText()),
                            TextoPuerta.getText(), Integer.parseInt(TextoTelefono.getText()));

                    newInversor.setApellido1(TextoAp1.getText());
                    newInversor.setApellido2(TextoAp2.getText());
                    newInversor.setCalle(TextoCalle.getText());
                    newInversor.setDNI(TextoDNI.getText());
                    newInversor.setNombre(TextoNombre.getText());
                    newInversor.setPiso(Integer.parseInt(TextoPiso.getText()));
                    newInversor.setPortal(Integer.parseInt(TextoPortal.getText()));
                    newInversor.setPuerta(TextoPuerta.getText());
                    newInversor.setTelefono(Integer.parseInt(TextoTelefono.getText()));
                    newInversor.getId_usuario().setId_usuario(TextoID.getText());

                    fa.modificarUsuario(newInversor.getId_usuario(), idAntiguo);
                    fa.modificarDatosInversor(newInversor);
                    


                    inversor = fa.consultarUnInversor(fa.consultarUnUsuario(TextoID.getText()));



                    fa.muestraExcepcion("Inversor modificado correctamente.");
                    etiquetaResultadoAccion.setVisible(false);
                }else{
                    fa.muestraExcepcion("Introduzca números válidos.");
                }

            } catch (NumberFormatException e) {
                etiquetaResultadoAccion.setText("Alguno de los campos teléfono, portal y piso tienen letras.");
                etiquetaResultadoAccion.setVisible(true);
            }
        } else {
            etiquetaResultadoAccion.setText("Completa todos los campos.");
            etiquetaResultadoAccion.setVisible(true);

        }

    }
