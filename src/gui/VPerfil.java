    private void btnDarBajaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDarBajaMouseClicked
                                      
        // TODO add your handling code here:
        Usuario user = inversor.getId_usuario();
        if(!fa.usuarioPoseeAcciones(user)){
            user.setEnEspera(-1);
            fa.modificarUsuario(user, user.getId_usuario());
            this.dispose();
            fa.muestraExcepcion("Ha solicitado darse de baja correctamente.");            
            fa.iniciaInterfazUsuario();

        }else{
            fa.muestraExcepcion("No se ha podido dar de baja. Compruebe sus participaciones activas.");
        }
    }//GEN-LAST:event_btnDarBajaMouseClicked

